package com.indosat.umb.model;

public class LoginModel {
	
	private String loginId;
	private String userName;
	private String userPassword;
	private String userRole;
	private String createdOn;
	private String userStatus;
	private String lastPasswordChangedOn;
	private String resetFlag;
	private String currentStatus;
	
	public String getLoginId() {
		return loginId;
	}
	public void setLoginId(String loginId) {
		this.loginId = loginId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserRole() {
		return userRole;
	}
	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}
	public String getCreatedOn() {
		return createdOn;
	}
	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}
	public String getUserStatus() {
		return userStatus;
	}
	public void setUserStatus(String userStatus) {
		this.userStatus = userStatus;
	}
	public String getLastPasswordChangedOn() {
		return lastPasswordChangedOn;
	}
	public void setLastPasswordChangedOn(String lastPasswordChangedOn) {
		this.lastPasswordChangedOn = lastPasswordChangedOn;
	}
	public String getResetFlag() {
		return resetFlag;
	}
	public void setResetFlag(String resetFlag) {
		this.resetFlag = resetFlag;
	}
	public String getCurrentStatus() {
		return currentStatus;
	}
	public void setCurrentStatus(String currentStatus) {
		this.currentStatus = currentStatus;
	}
	
	
		
	
}
