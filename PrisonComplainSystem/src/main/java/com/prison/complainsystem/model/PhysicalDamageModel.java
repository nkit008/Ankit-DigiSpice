package com.prison.complainsystem.model;

public class PhysicalDamageModel {

	private String prison_name;
	private String prison_id;
	private String comp_loggby;
	private String designation;
	private long phone;
	private String mail;
	private String damage_item;
	private String serial_no;
	private String damage_desc;

	
	public String getPrison_name() {
		return prison_name;
	}
	public void setPrison_name(String prison_name) {
		this.prison_name = prison_name;
	}
	public String getPrison_id() {
		return prison_id;
	}
	public void setPrison_id(String prison_id) {
		this.prison_id = prison_id;
	}
	public String getComp_loggby() {
		return comp_loggby;
	}
	public void setComp_loggby(String comp_loggby) {
		this.comp_loggby = comp_loggby;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public long getPhone() {
		return phone;
	}
	public void setPhone(long phone) {
		this.phone = phone;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getDamage_item() {
		return damage_item;
	}
	public void setDamage_item(String damage_item) {
		this.damage_item = damage_item;
	}
	public String getSerial_no() {
		return serial_no;
	}
	public void setSerial_no(String serial_no) {
		this.serial_no = serial_no;
	}
	public String getDamage_desc() {
		return damage_desc;
	}
	public void setDamage_desc(String damage_desc) {
		this.damage_desc = damage_desc;
	}
	

}
