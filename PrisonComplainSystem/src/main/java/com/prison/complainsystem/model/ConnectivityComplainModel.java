package com.prison.complainsystem.model;

public class ConnectivityComplainModel {
	
	private String prison_name;
	private String prison_id;
	private String comp_loggby;
	private String designation;
	private long phone;
	private String mail;
	private String connectivity_type;
	private String complain_desc;

	
	public String getPrison_name() {
		return prison_name;
	}
	public void setPrison_name(String prison_name) {
		this.prison_name = prison_name;
	}
	public String getPrison_id() {
		return prison_id;
	}
	public void setPrison_id(String prison_id) {
		this.prison_id = prison_id;
	}
	public String getComp_loggby() {
		return comp_loggby;
	}
	public void setComp_loggby(String comp_loggby) {
		this.comp_loggby = comp_loggby;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public long getPhone() {
		return phone;
	}
	public void setPhone(long phone) {
		this.phone = phone;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getConnectivity_type() {
		return connectivity_type;
	}
	public void setConnectivity_type(String connectivity_type) {
		this.connectivity_type = connectivity_type;
	}
	public String getComplain_desc() {
		return complain_desc;
	}
	public void setComplain_desc(String complain_desc) {
		this.complain_desc = complain_desc;
	}
	
	
	
	
	
}
